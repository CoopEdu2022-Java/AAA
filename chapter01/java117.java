
public class java117 {

    public static void main(String[] args) {
        int n = 0;
        if (n == 0) {
            System.out.println("1");

        } else {
            int x = 1;
            for (int i = n; i > 1; i--) {
                x = x * i;
            }
            System.out.println(x);
        }
    }

}
